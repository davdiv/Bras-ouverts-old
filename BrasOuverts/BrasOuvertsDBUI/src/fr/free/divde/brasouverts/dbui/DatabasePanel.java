    private JComboBox initJdbcDrivers() {
        Lookup.Result<Driver> drivers = Lookup.getDefault().lookupResult(Driver.class);
        Set<Class<? extends Driver>> classes = drivers.allClasses();
        String[] suggestions = new String[classes.size()];
        int i = 0;
        for (Class<? extends Driver> driver : classes) {
            suggestions[i] = driver.getName();
            i++;
        }
        JComboBox res = new JComboBox(suggestions);
        return res;
    }
