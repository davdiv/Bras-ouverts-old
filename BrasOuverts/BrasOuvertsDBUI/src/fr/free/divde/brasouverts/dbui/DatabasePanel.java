    void load() {
        BrasOuvertsDB.checkConnectionPreferences();
        Preferences prefs = NbPreferences.forModule(BrasOuvertsDB.class);
        jdbcDriverField.setSelectedItem(prefs.get("javax.persistence.jdbc.driver", ""));
        urlField.setText(prefs.get("javax.persistence.jdbc.url", ""));
        userNameField.setText(prefs.get("javax.persistence.jdbc.user", ""));
        passwordField.setText(prefs.get("javax.persistence.jdbc.password", ""));
    }
    
    void store() {
        Preferences prefs = NbPreferences.forModule(BrasOuvertsDB.class);
        prefs.put("javax.persistence.jdbc.driver", jdbcDriverField.getSelectedItem().toString());
        prefs.put("javax.persistence.jdbc.url", urlField.getText());
        prefs.put("javax.persistence.jdbc.user", userNameField.getText());
        prefs.put("javax.persistence.jdbc.password", passwordField.getText());
    }
    
